using System;

namespace Glimpse.Core.Extensibility
{
    /// <summary>
    /// An <see cref="ILogger"/> implementation of the Null Object pattern for use when Glimpse if configured to not use logging.
    /// </summary>
    public class NullLogger : ILogger
